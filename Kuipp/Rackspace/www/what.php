<p class="title">What is Kuipp?</p>

	<p class="highlight">Kuipp is a location-based, question & answer platform that provides users with a fun, fast way to ask and answer questions to discover what's currently happening in a specific area. Kuipp is ideal for local communities, organizations, tourists and those new to the area.</p>

	 <p class="video"><iframe width="560" height="349" src="http://www.youtube.com/embed/dqUdmqOsL7c" frameborder="0" allowfullscreen></iframe></p>

<p class="title">Features</p>

<p class="subtitle">Ask</p>

	<p>Communicate with local users to receive fast answers to your questions. Have a question in another area? No problem, you can easily tag your question to a location you want such that local users around nearby will be able to respond. We understand that sometimes your questions are not necessarily around your locality and may be somewhere else (ex. traveling to another city for the weekend)</p>

<p class="subtitle">Answers</p>

	<p>Help provide answers to questions in a variety of categories, such as:
	<ul>
	<li>Where's a great place to take a date/family for dinner in the city?
	<li>Are there any good venues for live music tonight?
	<li>I'm at the mall, are there any good discounts for clothes?
	<li>What's up with this traffic jam in the financial district?</ul></p>

	<p>View questions in a List, visually on a Map, or even sorted by a specific Keyword to help you filter through the local chatter to hone in on a specific topic or interest</p>

<p class="subtitle">Gaming</p>

	<p>Compete with other users and your friends to earn points. Points can be earned in a variety of ways by asking/answering questions and receiving likes to your answers. As you increase your points, users will unlock <b>Characters</b> to show everyone their knowledge status, as well as boost their <b>Influence</b> rating. In addition to this, a variety of <b>Medals</b> are available for users to unlock by performing certain achievements, such as asking/answer questions in a certain amount of time.</p>

